import java.util.*;

public class switch_case {

    public static void main(String[] args) {
        int num = 2;
        switch (num) {
            case 1:
                System.out.println("hi");
                break;
            case 2:
                System.out.println("Hello");
                break;
            case 3:
                System.out.println("Namaste");
                break;
            default:
                System.out.println("Wrong input");

        }

    }
}
