
public class Assignment_operator {
    public static void main(String[] args) {

        int a = 10;
        a += 5; // a= a+5
        int b = 15;
        b -= 5;// b= b-5
        int c = 4;
        c *= 5; // c= c*5
        int d = 50;
        d /= 5; // d= d/5
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }

}
