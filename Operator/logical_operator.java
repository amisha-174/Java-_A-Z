
public class logical_operator {
    public static void main(String[] args) {
        // logical operator are &&(logical AND) , ||(logical OR), !(logical NOT )

        System.out.println((3 > 2) && (6 > 5));
        System.out.println((3 < 2) || (6 > 5));
        System.out.println(!(3 > 2));
    }

}
